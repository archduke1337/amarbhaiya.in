const fs = require("fs")
const path = require("path")
const { Client, Databases, Storage, ID } = require("node-appwrite")

const ROOT = process.cwd()
const ENV_FILES = [path.join(ROOT, ".env"), path.join(ROOT, ".env.local")]
const ENV_LOCAL_PATH = path.join(ROOT, ".env.local")

function parseEnv(content) {
  const env = {}
  for (const line of content.split(/\r?\n/)) {
    const trimmed = line.trim()
    if (!trimmed || trimmed.startsWith("#")) continue
    const idx = trimmed.indexOf("=")
    if (idx <= 0) continue
    const key = trimmed.slice(0, idx).trim()
    const raw = trimmed.slice(idx + 1).trim()
    env[key] = raw.replace(/^['\"]|['\"]$/g, "")
  }
  return env
}

function loadEnv() {
  const merged = {}
  for (const file of ENV_FILES) {
    if (fs.existsSync(file)) {
      Object.assign(merged, parseEnv(fs.readFileSync(file, "utf8")))
    }
  }
  Object.assign(merged, process.env)
  return merged
}

function isPlaceholder(value) {
  if (!value) return true
  const lower = String(value).toLowerCase()
  return lower.includes("your_") || lower.includes("_here") || lower === "changeme"
}

function ensureLocalEnvFile() {
  if (!fs.existsSync(ENV_LOCAL_PATH)) {
    fs.writeFileSync(ENV_LOCAL_PATH, "# Generated by scripts/setup-appwrite.js\n")
  }
}

function upsertEnvVar(key, value) {
  ensureLocalEnvFile()
  const content = fs.readFileSync(ENV_LOCAL_PATH, "utf8")
  const re = new RegExp(`^${key}=.*$`, "m")
  const line = `${key}=${value}`
  const next = re.test(content) ? content.replace(re, line) : `${content.trimEnd()}\n${line}\n`
  fs.writeFileSync(ENV_LOCAL_PATH, next)
}

function defaultId(name) {
  return name.toLowerCase().replace(/[^a-z0-9]+/g, "_")
}

async function getOrCreateDatabase(databases, env) {
  const configured = env.APPWRITE_DATABASE_ID
  const dbId = isPlaceholder(configured) ? "amarbhaiya_lms" : configured

  try {
    await databases.get(dbId)
    return { id: dbId, created: false }
  } catch {
    await databases.create(dbId, "AmarBhaiya LMS")
    if (isPlaceholder(configured)) upsertEnvVar("APPWRITE_DATABASE_ID", dbId)
    return { id: dbId, created: true }
  }
}

async function getOrCreateCollection(databases, databaseId, envKey, fallbackId, name, env) {
  const configured = env[envKey]
  const collectionId = isPlaceholder(configured) ? fallbackId : configured

  try {
    await databases.getCollection(databaseId, collectionId)
    return { id: collectionId, created: false }
  } catch {
    await databases.createCollection(databaseId, collectionId, name, [], true, true)
    if (isPlaceholder(configured)) upsertEnvVar(envKey, collectionId)
    return { id: collectionId, created: true }
  }
}

async function getOrCreateBucket(storage, envKey, fallbackId, name, env) {
  const configured = env[envKey]
  const bucketId = isPlaceholder(configured) ? fallbackId : configured

  try {
    await storage.getBucket(bucketId)
    return { id: bucketId, created: false }
  } catch {
    await storage.createBucket(bucketId, name, [], true, true)
    if (isPlaceholder(configured)) upsertEnvVar(envKey, bucketId)
    return { id: bucketId, created: true }
  }
}

async function main() {
  const env = loadEnv()

  const endpoint = env.NEXT_PUBLIC_APPWRITE_ENDPOINT
  const projectId = env.NEXT_PUBLIC_APPWRITE_PROJECT_ID
  const apiKey = env.APPWRITE_API_KEY

  if (!endpoint || !projectId || !apiKey || isPlaceholder(projectId) || isPlaceholder(apiKey)) {
    throw new Error(
      "Missing required Appwrite credentials. Set NEXT_PUBLIC_APPWRITE_ENDPOINT, NEXT_PUBLIC_APPWRITE_PROJECT_ID, APPWRITE_API_KEY in .env/.env.local"
    )
  }

  const client = new Client().setEndpoint(endpoint).setProject(projectId).setKey(apiKey)
  const databases = new Databases(client)
  const storage = new Storage(client)

  const collections = [
    ["APPWRITE_COLLECTION_USERS", "users", "Users"],
    ["APPWRITE_COLLECTION_COURSES", "courses", "Courses"],
    ["APPWRITE_COLLECTION_CATEGORIES", "categories", "Categories"],
    ["APPWRITE_COLLECTION_MODULES", "modules", "Modules"],
    ["APPWRITE_COLLECTION_LESSONS", "lessons", "Lessons"],
    ["APPWRITE_COLLECTION_RESOURCES", "resources", "Resources"],
    ["APPWRITE_COLLECTION_ENROLLMENTS", "enrollments", "Enrollments"],
    ["APPWRITE_COLLECTION_PROGRESS", "progress", "Progress"],
    ["APPWRITE_COLLECTION_QUIZZES", "quizzes", "Quizzes"],
    ["APPWRITE_COLLECTION_QUIZ_QUESTIONS", "quiz_questions", "Quiz Questions"],
    ["APPWRITE_COLLECTION_QUIZ_ATTEMPTS", "quiz_attempts", "Quiz Attempts"],
    ["APPWRITE_COLLECTION_ASSIGNMENTS", "assignments", "Assignments"],
    ["APPWRITE_COLLECTION_SUBMISSIONS", "submissions", "Submissions"],
    ["APPWRITE_COLLECTION_CERTIFICATES", "certificates", "Certificates"],
    ["APPWRITE_COLLECTION_LIVE_SESSIONS", "live_sessions", "Live Sessions"],
    ["APPWRITE_COLLECTION_SESSION_RSVPS", "session_rsvps", "Session RSVPs"],
    ["APPWRITE_COLLECTION_COURSE_COMMENTS", "course_comments", "Course Comments"],
    ["APPWRITE_COLLECTION_FORUM_CATEGORIES", "forum_categories", "Forum Categories"],
    ["APPWRITE_COLLECTION_FORUM_THREADS", "forum_threads", "Forum Threads"],
    ["APPWRITE_COLLECTION_FORUM_REPLIES", "forum_replies", "Forum Replies"],
    ["APPWRITE_COLLECTION_PAYMENTS", "payments", "Payments"],
    ["APPWRITE_COLLECTION_SUBSCRIPTIONS", "subscriptions", "Subscriptions"],
    ["APPWRITE_COLLECTION_MODERATION_ACTIONS", "moderation_actions", "Moderation Actions"],
    ["APPWRITE_COLLECTION_AUDIT_LOGS", "audit_logs", "Audit Logs"],
    ["APPWRITE_COLLECTION_NOTIFICATIONS", "notifications", "Notifications"],
    ["APPWRITE_COLLECTION_COURSE_REVIEWS", "course_reviews", "Course Reviews"],
  ]

  const buckets = [
    ["APPWRITE_BUCKET_COURSE_VIDEOS", "course_videos", "Course Videos"],
    ["APPWRITE_BUCKET_COURSE_THUMBNAILS", "course_thumbnails", "Course Thumbnails"],
    ["APPWRITE_BUCKET_LESSON_RESOURCES", "lesson_resources", "Lesson Resources"],
    ["APPWRITE_BUCKET_CERTIFICATES", "certificates", "Certificates"],
    ["APPWRITE_BUCKET_AVATARS", "avatars", "Avatars"],
    ["APPWRITE_BUCKET_BLOG_IMAGES", "blog_images", "Blog Images"],
  ]

  const db = await getOrCreateDatabase(databases, env)

  let createdCollections = 0
  let existingCollections = 0
  for (const [envKey, fallbackId, displayName] of collections) {
    const result = await getOrCreateCollection(databases, db.id, envKey, fallbackId, displayName, env)
    if (result.created) createdCollections += 1
    else existingCollections += 1
  }

  let createdBuckets = 0
  let existingBuckets = 0
  for (const [envKey, fallbackId, displayName] of buckets) {
    const result = await getOrCreateBucket(storage, envKey, fallbackId, displayName, env)
    if (result.created) createdBuckets += 1
    else existingBuckets += 1
  }

  console.log("[setup-appwrite] Completed")
  console.log(`[setup-appwrite] Database: ${db.id} (${db.created ? "created" : "exists"})`)
  console.log(`[setup-appwrite] Collections: created=${createdCollections}, existing=${existingCollections}`)
  console.log(`[setup-appwrite] Buckets: created=${createdBuckets}, existing=${existingBuckets}`)
  console.log("[setup-appwrite] Missing IDs (if any) were written to .env.local")
}

main().catch((err) => {
  console.error("[setup-appwrite] Failed:", err.message)
  process.exit(1)
})
